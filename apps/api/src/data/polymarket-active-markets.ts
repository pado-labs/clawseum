function makeVariant(base: ExternalActiveMarket, variantIndex: number): ExternalActiveMarket {
  const decay = Math.max(0.35, 1 - variantIndex * 0.08);
  return {
    topic: `${base.topic} (Alt ${variantIndex})`,
    category: base.category,
    volume: Math.max(500000, Math.floor(base.volume * decay)),
  };
}

export function buildPolymarketActiveMarkets(targetCount = 200): ExternalActiveMarket[] {
  if (BASE_ACTIVE_MARKETS.length >= targetCount) {
    return BASE_ACTIVE_MARKETS.slice(0, targetCount);
  }

  const out = [...BASE_ACTIVE_MARKETS];
  let cursor = 0;
  let variant = 1;

  while (out.length < targetCount) {
    const base = BASE_ACTIVE_MARKETS[cursor % BASE_ACTIVE_MARKETS.length];
    if (!base) {
      break;
    }
    out.push(makeVariant(base, variant));
    cursor += 1;
    if (cursor % BASE_ACTIVE_MARKETS.length === 0) {
      variant += 1;
    }
  }

  return out;
}

